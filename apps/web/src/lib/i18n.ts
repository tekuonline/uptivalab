export type Language = keyof typeof translations;
export type TranslationKey = keyof (typeof translations)["en"];

let currentLanguage: Language = "en";
let listeners: Array<() => void> = [];

export function setLanguage(lang: Language) {
  currentLanguage = lang;
  document.documentElement.lang = lang;
  // Notify all listeners
  listeners.forEach(listener => listener());
}

export function getLanguage(): Language {
  return currentLanguage;
}

export function onLanguageChange(callback: () => void) {
  listeners.push(callback);
  return () => {
    listeners = listeners.filter(l => l !== callback);
  };
}

export function t(key: TranslationKey): string {
  const lang = translations[currentLanguage] as Record<string, string>;
  const fallback = translations.en;
  return (lang?.[key] ?? fallback[key] ?? key) as string;
}

export function initI18n(lang?: string) {
  const language = (lang || "en") as Language;
  if (language in translations) {
    setLanguage(language);
  }
}

// React hook for translations with reactivity
export function useTranslation() {
  const [language, setCurrentLanguage] = useState(getLanguage());
  
  useEffect(() => {
    const unsubscribe = onLanguageChange(() => {
      setCurrentLanguage(getLanguage());
    });
    return unsubscribe;
  }, []);
  
  return {
    t: (key: TranslationKey) => {
      const lang = translations[language] as Record<string, string>;
      const fallback = translations.en;
      return (lang?.[key] ?? fallback[key] ?? key) as string;
    },
    language,
    setLanguage,
  };
}
