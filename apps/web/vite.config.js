import { defineConfig, loadEnv } from "vite";
import react from "@vitejs/plugin-react";
export default defineConfig(({ mode }) => {
    const env = loadEnv(mode, process.cwd(), "VITE_");
    return {
        plugins: [react()],
        server: {
            port: Number(env.VITE_DEV_SERVER_PORT ?? 5173),
        },
        build: {
            outDir: "dist",
            sourcemap: true,
        },
    };
});
